/**********************************************************************

	ATOI - Convert string to Integer

**********************************************************************/

int atoi (s) char s[];

	{int i, f, c;
	
	if (!s) return (0);
	i = f = 0;
	if (*s == '-') {++s; ++f;}
	while ((c = *s++)>='0' && c<='9') i = i*10 + c-'0';
	return (f?-i:i);
	}
